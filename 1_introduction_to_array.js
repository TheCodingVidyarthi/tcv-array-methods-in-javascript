/**
 * Array in JavaScript
 * An array is a data structure that stores  collection of items
 */

/**
 * To define an array we use opening and closing square bracket `[]`
 * Since there are no items present inside the bracket it is an empty array
 */
let emptyArray = [];

/**
 * Defining an array and assigning values.
 * In the below example the array has 4 items
 */
let fruits = ['Apple', 'Banana', 'Orange', 'Mango'];

/**
 * An array containing mixed datatypes
 * In the below example the array contains number, String, boolean, object, array
 */
let mixedArray = [1, 'Hello', true, { key: 'value' }, [5, 10]];